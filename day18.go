package main

import (
	"bufio"
	"fmt"
	"strings"
	"time"
)

type point struct {
	y, x int
}

type board map[point]bool

func (mPtr *board) tick() {
	m := *mPtr
	// Copy current state of the board.
	currentBoard := make(board)
	for key, value := range m {
		currentBoard[key] = value
	}

	// Iterate the current board.
	for key, value := range currentBoard {
		nCount := currentBoard.neighborCount(key)
		if value && nCount != 2 && nCount != 3 {
			m[key] = false
		}
		if !value && nCount == 3 {
			m[key] = true
		}
	}
}

// Count the number of neightbors for a given point, without counting self.
func (mPtr *board) neighborCount(p point) int {
	m := *mPtr
	result := 0
	for y := -1; y <= 1; y++ {
		for x := -1; x <= 1; x++ {
			if x == 0 && y == 0 {
				continue
			}
			if m[point{y + p.y, x + p.x}] {
				result++
			}
		}
	}
	return result
}

// Returns the total number of lights on, in the grid.
func (mPtr *board) lightsOn() int {
	m := *mPtr
	result := 0
	for _, value := range m {
		if value {
			result++
		}
	}
	return result
}

func main() {
	m := make(board)

	// Parse the board.
	scanner := bufio.NewScanner(strings.NewReader(input))
	for row := 0; scanner.Scan(); row++ {
		line := scanner.Text()
		for idx, char := range line {
			m[point{row, idx}] = char == '#'
		}
	}

	// Make a copy of the board, for part 2.
	m2 := make(board)
	for key, value := range m {
		m2[key] = value
	}

	before := time.Now()
	fmt.Println("Lights on before:", m.lightsOn())
	for i := 0; i < totalTicks; i++ {
		m.tick()
		m2.tick()
		// Turn corners on again, by incident the input has the corners
		// turned on to begin with. If your input differs, turn them on before
		// the first tick() on m2.
		m2[point{0, 0}] = true
		m2[point{0, 99}] = true
		m2[point{99, 99}] = true
		m2[point{99, 0}] = true
	}
	fmt.Println("Number of lights on after 100 iterations:", m.lightsOn())
	fmt.Println("NUmber of lights on, for part 2:", m2.lightsOn())
	fmt.Println("Took:", time.Now().Sub(before))
}

